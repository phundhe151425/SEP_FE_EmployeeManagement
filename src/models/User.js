export default class User {


    constructor(username, email, password, fullName, department, code, gender, role, cover) {
        this.username = username;
        this.email = email;
        this.password = password;
        this.fullName = fullName;
        this.department = department;
        this.code = code;
        this.gender = gender;
        this.role = role;
        this.cover = cover
    }
}